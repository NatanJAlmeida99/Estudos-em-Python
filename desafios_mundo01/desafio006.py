# Crie um algoritmo que leia um número e mostre o seu dobra, triplo e raiz quadrada
num = int(input("Digite um número: "))
dobro = num * 2
triplo = num * 3
raiz = num ** 0.5
print(f"O dobro de {num} vale {dobro}")
print(f"O triplo de {num} vale {triplo}")
print(f"A raiz quadrada de {num} vale {raiz:.2f}")